#' suma
#'
#' Function to add two numbers
#'
#' @param x first number
#' @param y second number

#' 
#' @examples
#' 
#' suma(x=3,y=5)
#' 
#' # Using numbers directly
#' suma(1,2)
#' 
#' #using assignments
#' x=2
#' y=6.8 
#' suma(x,y)
#'
#' @export

suma <- function(x, y) {
  s <- x + y
  return(s)
}

